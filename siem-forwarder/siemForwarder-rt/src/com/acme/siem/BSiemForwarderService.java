/**
 * BSiemForwarderService
 * ---------------------
 * A station service that forwards security-relevant events (point value/status
 * changes, alarms, audit records) to an external SIEM over syslog/TLS.
 *
 * Design constraints (monitoring must NOT interfere with normal JACE operation):
 *
 *   1. RIDE-ALONG ONLY   - We never subscribe a proxy point ourselves. Doing so
 *                          would push it into the driver's poll queue and add
 *                          traffic to the RS-485 trunk (degrading MS/TP token
 *                          timing / Modbus poll cycles). RideAlongSubscriber only
 *                          forwards points that some OTHER component already
 *                          subscribed (graphics, histories, control logic).
 *
 *   2. BOUNDED QUEUE     - Event handlers do nothing but enqueue() and return in
 *                          microseconds. All socket I/O happens on ForwardWorker,
 *                          a single low-priority thread. The queue is bounded and
 *                          drop-oldest: under pressure, SIEM data is shed, never
 *                          engine time. Drops are counted and reported.
 *
 *   3. SELF-THROTTLE     - SelfThrottleMonitor watches engine-hog % and free heap.
 *                          When the station is under load it throttles or suspends
 *                          forwarding (fail-open) and emits a heartbeat so the SIEM
 *                          knows why the feed paused. Control always wins.
 *
 * Place this service under the station's Services container.
 *
 * @author skeleton
 */
