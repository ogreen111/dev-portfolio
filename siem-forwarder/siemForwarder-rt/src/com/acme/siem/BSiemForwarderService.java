  /**
   * Single entry point every event handler funnels through. Returns immediately;
   * the heavy lifting (format + socket write) happens on the worker thread.
   * Honors the self-throttle in BOTH degraded states: THROTTLING admits only
   * warning-and-above, SUSPENDED drops everything except the periodic heartbeat
   * that tells the SIEM the feed is intentionally paused.
   */
  void offer(SiemEvent evt)
  {
    ForwardWorker w = worker;                   // local copy: stop() may null the field
    if (w == null) return;
    SelfThrottleMonitor m = monitor;
    if (m != null && !m.admits(evt))
    {
      w.countDrop();                            // throttled/suspended: shed, keep the tally
      return;
    }
    w.enqueue(evt);
  }

